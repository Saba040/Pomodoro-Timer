# This is a graphical Pomodoro Timer application built
# with PyQt5, designed to help with time management and
# focus. The app follows the Pomodoro technique with
# 25-minute work sessions followed by 5-minute breaks.
# It automatically notifies the user when a session ends
# and starts the break timer. Optionally, relaxing music
# can be played during breaks for a better experience.